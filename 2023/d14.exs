defmodule D14 do
  def part2(input) do
    m = input
    |> String.split("\n", trim: true)
    |> Enum.map(&to_charlist/1)
    Enum.reduce_while(1..1000000000, {m, %{}}, fn i, {a, h} ->
      b = cycle(a)
      case h[b] do
        nil -> {:cont, {b, Map.put(h, b, i)}}
        j -> {:halt, find(i, j, h)}
      end
    end)
  end

  def find(i, j, h) do
    Enum.find_value(h, fn {m, n} -> n == rem(1000000000 - j, i - j) + j && eval(m) end)
  end

  def print(m) do
    IO.puts(Enum.join(m, "\n"))
    IO.puts("score: #{eval m}\n")
    m
  end

  def cycle(m) do
    for _ <- 1..4, reduce: m do
      a -> roll(a) |> Enum.zip_with(&Enum.reverse &1)
    end
    #|> print()
  end

  def roll(m) do
    m
    |> Enum.zip_with(& &1)
    |> Enum.map(fn r ->
      n = length(r)
      Enum.reduce(r, {n, n, []}, fn c, {h, i, a} ->
        case c do
          ?O -> {h - 1, i - 1, [{?O, h} | a]}
          ?# -> {i - 1, i - 1, [{?#, i} | a]}
          ?. -> {h, i - 1, a}
        end
      end)
      |> elem(2)
      |> then(&Enum.reduce(1..n, {&1, []},
      fn i, {[{c, i} | a], b} -> {a, [c | b]}
         _, {a, b} -> {a, [?. | b]}
      end))
      |> elem(1)
    end)
      |> Enum.zip_with(& &1)
  end

  def eval(m) do
    Enum.zip_reduce(m, length(m)..1, 0, fn r, i, s ->
      s + i * Enum.count(r, & &1 == ?O)
    end)
  end
end

